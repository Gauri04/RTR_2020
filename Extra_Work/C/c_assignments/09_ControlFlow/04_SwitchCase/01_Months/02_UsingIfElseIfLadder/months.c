#include<stdio.h>
int main(void)
{
	int num_month;
	
	printf("\n Enter number of month : ");
	scanf("%d", &num_month);
	
	if(num_month == 1)
	{
		printf("\n Month number %d is January", num_month);
	}
	
	else if(num_month == 2)
	{
		printf("\n Month number %d is February", num_month);
	}
	
	else if(num_month == 3)
	{
		printf("\n Month number %d is March", num_month);
	}
	
	else if(num_month == 4)
	{
		printf("\n Month number %d is April", num_month);
	}
	
	else if(num_month == 5)
	{
		printf("\n Month number %d is May", num_month);
	}
	
	else if(num_month == 6)
	{
		printf("\n Month number %d is June", num_month);
	}
	
	else if(num_month == 7)
	{
		printf("\n Month number %d is July", num_month);
	}
	
	else if(num_month == 8)
	{
		printf("\n Month number %d is August", num_month);
	}
	
	else if(num_month == 9)
	{
		printf("\n Month number %d is September", num_month);
	}
	
	else if(num_month == 10)
	{
		printf("\n Month number %d is October", num_month);
	}
	
	else if(num_month == 11)
	{
		printf("\n Month number %d is November", num_month);
	}
	
	else if(num_month == 12)
	{
		printf("\n Month number %d is December", num_month);
	}
	
	else
	{
		printf("\n Invalid month number");
	}
	
	printf("\n If else if ladder complete");
	return(0);
	
}

/* output *
 Enter number of month : 5

 Month number 5 is May
 If else if ladder complete
 */












